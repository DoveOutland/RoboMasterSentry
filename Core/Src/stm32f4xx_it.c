void HAL_UART_RxCpltCallback(UART_HandleTypeDef* huart)
{

	if (huart->Instance == USART1)
	{
		GimbalRxBuffer[Uart1_Rx_Cnt++] = aRxBuffer;

		if (GimbalRxBuffer[Uart1_Rx_Cnt - 1] == 0x45)
		{

			if (GimbalRxBuffer[0] == 0x53)
			{
				GimbalRealBuf[0] = GimbalRxBuffer[1]; /* 是否有目标 */
				GimbalRealBuf[1] = GimbalRxBuffer[2]; /* 是否开火  */
				GimbalRealBuf[2] = GimbalRxBuffer[3]; /* yaw正负 正:1 负:0  */
				GimbalRealBuf[3] = GimbalRxBuffer[5] << 8 | GimbalRxBuffer[4]; /* yaw偏差  */
				GimbalRealBuf[4] = GimbalRxBuffer[6]; /* pitch正负 正:1 负:0  */
				GimbalRealBuf[5] = GimbalRxBuffer[8] << 8 | GimbalRxBuffer[7]; /*  pitch偏差  */
				GimbalRealBuf[6] = GimbalRxBuffer[10] << 8 | GimbalRxBuffer[9]; /*  距离  */
//				HAL_GPIO_TogglePin(GLED_GPIO_Port,GLED_Pin);
				Uart1_Rx_Cnt = 0;
				memset(GimbalRxBuffer, 0x00, sizeof(GimbalRxBuffer));
			}
			else
			{
				Uart1_Rx_Cnt = 0;
			}
		}
	}

	HAL_UART_Receive_IT(&huart1, &aRxBuffer, sizeof(aRxBuffer));
}


